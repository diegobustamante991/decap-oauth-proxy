/**
 * Compiles every document in ../knowledge into api/_knowledge.extra.js so the
 * agent can use them server-side.
 *
 * Supported: .md, .markdown, .txt, .json (text), .pdf (pdf-parse), .docx (mammoth).
 *
 * Run: node scripts/build-knowledge.mjs  (or `npm run build:knowledge`)
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const knowledgeDir = path.join(here, '..', 'knowledge');
const outFile = path.join(here, '..', 'api', '_knowledge.extra.js');

const TEXT_EXTS = new Set(['.md', '.markdown', '.txt', '.json', '.csv']);
const BINARY_EXTS = new Set(['.pdf', '.docx', '.xlsx', '.xls']);

/** Clean up extracted text: collapse excessive blank lines/whitespace. */
function tidy(text) {
  return text
    .replace(/\r\n/g, '\n')
    .replace(/[ \t]+\n/g, '\n')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

async function extractPdf(filePath) {
  const mod = await import('pdf-parse/lib/pdf-parse.js');
  const pdfParse = mod.default || mod;
  const data = await pdfParse(fs.readFileSync(filePath));
  return data.text || '';
}

async function extractDocx(filePath) {
  const mod = await import('mammoth');
  const mammoth = mod.default || mod;
  const { value } = await mammoth.extractRawText({ buffer: fs.readFileSync(filePath) });
  return value || '';
}

async function extractSpreadsheet(filePath) {
  const mod = await import('xlsx');
  const XLSX = mod.default || mod;
  const wb = XLSX.read(fs.readFileSync(filePath));
  let out = '';
  for (const name of wb.SheetNames) {
    const csv = XLSX.utils.sheet_to_csv(wb.Sheets[name]);
    if (csv.trim()) out += `\n### Sheet: ${name}\n${csv}\n`;
  }
  return out;
}

async function extract(filePath, ext) {
  if (TEXT_EXTS.has(ext)) return fs.readFileSync(filePath, 'utf8');
  if (ext === '.pdf') return extractPdf(filePath);
  if (ext === '.docx') return extractDocx(filePath);
  if (ext === '.xlsx' || ext === '.xls') return extractSpreadsheet(filePath);
  return '';
}

let combined = '';
let fileCount = 0;

if (fs.existsSync(knowledgeDir)) {
  const files = fs
    .readdirSync(knowledgeDir)
    .filter((f) => f.toLowerCase() !== 'readme.md')
    .sort();

  for (const file of files) {
    const ext = path.extname(file).toLowerCase();
    if (!TEXT_EXTS.has(ext) && !BINARY_EXTS.has(ext)) continue;
    try {
      const raw = tidy(await extract(path.join(knowledgeDir, file), ext));
      if (!raw) continue;
      const title = path.basename(file, path.extname(file)).replace(/[-_]/g, ' ');
      combined += `\n\n## ${title}\n${raw}`;
      fileCount += 1;
    } catch (err) {
      console.warn(`[build-knowledge] could not read ${file}: ${err.message}`);
    }
  }
}

const header = combined.trim()
  ? '# Additional private context about Diego\n' +
    '(Use these facts the same way as the main knowledge base.)' +
    combined
  : '';

const body =
  '/**\n' +
  ' * AUTO-GENERATED by scripts/build-knowledge.mjs. Do not edit by hand.\n' +
  ' * Source: the /knowledge folder.\n' +
  ' */\n' +
  `export const EXTRA_KNOWLEDGE = ${JSON.stringify(header)};\n`;

fs.writeFileSync(outFile, body);
console.log(`[build-knowledge] compiled ${fileCount} file(s) into api/_knowledge.extra.js`);
